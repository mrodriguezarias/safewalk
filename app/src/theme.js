const theme = {
  header: colors.green,
  primary: colors.blue,
  accent: colors.blue,
  tabBar: colors.purple,
  safe: colors.yellow,
  marker: colors.grey,
  rogue: colors.red,
}

const getTheme = (appearance) => {
  return Object.keys(theme).reduce((accum, key) => {
    const value = theme[key][appearance]
    return { ...accum, [key]: value }
  }, {})
}

